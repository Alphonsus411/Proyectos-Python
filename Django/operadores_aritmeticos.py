# operadores aritméticos
# pedimos números al usuario

numero1 = int(input("Ingresa un número : "))
numero2 = int(input("Ingresa un  segundo número : "))

# imprimir resultados

# suma
print(f'La suma es : {numero1 + numero2}')

# resta
print(f'La resta es : {numero1 - numero2}')

# multiplicacion
print(f'La multiplicacion es : {numero1 * numero2}')

# division
print(f'La division es : {numero1 / numero2}')

# modulo
print(f'El modulo es : {numero1 % numero2}')


